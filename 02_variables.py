
# name_of_var = value_of_variable
var1 = "Hello world Fernando"

# variable of type string(text)
print(var1)
print("type(var1) = ", type(var1))

var2 = 2

# Varible of type int (Number non decimal)
print(var2)
print("type(var2) =", type(var2))

var3 = 4.3

# Variable of type float(Numeric decimal)
print(var3)
print("type(var3) =", type(var3))

var4 = True
var5 = False

# Variable of boolean type (True, False)
print("var4 =", var4)
print("var5 =", var5)
print("type(var4) =", type(var4))
print("type(var5) =", type(var5))

var6 = None

# Variable of type None
print("var6 =", var6)
